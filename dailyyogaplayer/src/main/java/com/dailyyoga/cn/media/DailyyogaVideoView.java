    public void setVideoURI(Uri uri, Map<String, String> headers) {
        Log.d(TAG, "uri: " + uri.toString());
        mUri = uri;
        mHeaders = headers;
        mSeekWhenPrepared = 0;
        openVideo();
        requestLayout();
        invalidate();
    }

